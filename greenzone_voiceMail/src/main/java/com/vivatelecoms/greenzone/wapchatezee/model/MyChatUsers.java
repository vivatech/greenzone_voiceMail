package com.vivatelecoms.greenzone.wapchatezee.model;

public class MyChatUsers {

	
	private String chatFriendUserId;
	private String chatFriendMsisdn;
	private String chatDate;
	private String chatMessage;
	private String gender;
	private String chatTime;
	
	public String getChatDate() {
		return chatDate;
	}
	public void setChatDate(String chatDate) {
		this.chatDate = chatDate;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getChatFriendUserId() {
		return chatFriendUserId;
	}
	public void setChatFriendUserId(String chatFriendUserId) {
		this.chatFriendUserId = chatFriendUserId;
	}
	public String getChatFriendMsisdn() {
		return chatFriendMsisdn;
	}
	public void setChatFriendMsisdn(String chatFriendMsisdn) {
		this.chatFriendMsisdn = chatFriendMsisdn;
	}
	public String getChatMessage() {
		return chatMessage;
	}
	public void setChatMessage(String chatMessage) {
		this.chatMessage = chatMessage;
	}
	public String getChatTime() {
		return chatTime;
	}
	public void setChatTime(String chatTime) {
		this.chatTime = chatTime;
	}
	
	

}
