public class MyContacts {
	private String friendMsisdn;
	private String friendUserId;
	private String AddFriendDate;
	private String AddFriendTime;
	private String gender;
	
	
	public String getFriendMsisdn() {
		return friendMsisdn;
	}
	public void setFriendMsisdn(String friendMsisdn) {
		this.friendMsisdn = friendMsisdn;
	}
	public String getFriendUserId() {
		return friendUserId;
	}
	public void setFriendUserId(String friendUserId) {
		this.friendUserId = friendUserId;
	}
	public String getAddFriendDate() {
		return AddFriendDate;
	}
	public void setAddFriendDate(String addFriendDate) {
		AddFriendDate = addFriendDate;
	}
	public String getAddFriendTime() {
		return AddFriendTime;
	}
	public void setAddFriendTime(String addFriendTime) {
		AddFriendTime = addFriendTime;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	
	

}
